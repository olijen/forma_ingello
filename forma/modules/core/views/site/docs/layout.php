  <h2><i class="fa fa-handshake"></i> Бизнес - это продажи</h2>
  <hr>
  <img src="/images/selling.jpg" style="width: 100%;">
  <hr>
  <p>В вертикальном меню слева зайдите в раздел "продажи". <a href="/selling/main">[ быстрая ссылка ]</a></p>
  <p>Тут можно отслеживать все продажи по статусам и смотреть детали, осуществлять поиск по дате или имени. А так же можно начать новую продажу.</p>
  <p>Сообщите разработчикам, если Вам нужно доработать данный раздел.</p>
  <p>Нажмите кнопку "Начать продажи" или используйте быструю ссылку <a href="/selling/form/index">[ быстрая ссылка ]</a></p>
  <p>Перед Вами открылась панель "Новая продажа" и два элемента для ввода.</p>
  <p>Слева выберите или создайте место продажи. Это может быть склад, филиал и т.д.</p>
  <p>Справа выберите или создайте клиента для продажи. Данный элемент позволяет осуществлять быстрый поиск по клиентской базе и выбор из выпадающего списка.</p>
  <p>Сообщите разработчикам, если какой либо из элементов лишний или нуждается в доработке.</p>
  <p>Нажмите "Сохранить".</p>
  <p>После сохранения Вам стали доступны 4 новых блока.</p>
  <p>Блок истории нужен для фиксирования диалога с клиентом и просмотра истории общения по скриптам. Нажмите "Начать разговор" для начала работы по скрипту или "История" для просмотра прошлых контактов.</p>
  <p>Блок "Состояния" нужен для обозначения стадии взаимоотношений с клиентом. Для перехода в новое состояние нажмите соответствующее название этапа.</p>
  <p>Блок "Товар" нужен для оперативного поиска товаров или услуг в базе данных и добавления к счёту клиента. Данные можно скачать (выгрузить) в нужный формат.</p>
  <p>Всё, добавленное в боке товар просуммируется и выведется в блоке "Итого".</p>
  <p>Сообщите разработчикам, если один из блоков нужно изменить или удалить.</p>
