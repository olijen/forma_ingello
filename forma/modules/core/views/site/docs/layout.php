  <h2><i class="fa fa-users"></i> Бизнес - это люди</h2>
  <hr>
  <img src="/images/people.jpg" style="width: 100%;">
  <hr>
  <p>Практически у каждого раздела или объекта в системе есть адрес. Вы можете меняться этими ссылками с сотрудниками и клиентами.</p>
  <p>Зайдите в раздел "люди" Для этого выберите в меню иконку с человеком и нажмите "Панель управления" или перейдите по ссылке <a href="/core/default/people">[ быстрая ссылка ]</a></p>
  <p>Перейдя по ссылке, Вы увидите разные типы людей. Сообщите разработчику, какие не используются Вами в работе и мы их удалим.</p>
  <p>Давайте зайдем в раздел клиенты, и поработаем с клиентской базой.</p>
  <p>Нажмите на кнопку "Клиенты" или перейдите по быстрой ссылке <a href="/customer/customer">[ быстрая ссылка ]</a></p>
  <p>Сейчас Вы находитесь в разделе клиентской базы. Вы можете искать людей в Вашей базе по любым заявленным параметрам - телефон, почта, дата рождения, возраст...</p>
  <p>Давайте добавим нового клиента. Нажмите на кнопку "Создать клиента" или перейдите по быстрой ссылке <a href="/customer/customer/create">[ быстрая ссылка ]</a></p>
  <p>Заполните поля и уточните разработчику, какие поля Вас интересуют дополнительно, а какие лишние. (выпадающие списки, выбор даты и диапазона, выбор цвета, загрузка файлов и так далее).</p>
